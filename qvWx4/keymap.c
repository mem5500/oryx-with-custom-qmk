uint16_t get_tapping_term(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case MT(MOD_LGUI, KC_A):
            return TAPPING_TERM + 10;
        case MT(MOD_LCTL, KC_F):
            return TAPPING_TERM -10;
        case KC_Z:
            return TAPPING_TERM -10;
        case MT(MOD_RCTL, KC_J):
            return TAPPING_TERM -10;
        case MT(MOD_RGUI, KC_SCLN):
            return TAPPING_TERM + 10;
        case KC_SLASH:
            return TAPPING_TERM + 10;
        default:
            return TAPPING_TERM;
    }
}


